#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;

	if (!s || !f)
		return ((void)0);
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
